package com.basicjava;

public class If {
    public static void main(String[] args) {
        int x = 10;
        if(x==10){
            System.out.println("x value is 10");
        }else if(x==12){
            System.out.println("x is not equal to 10");
        }else if(x==15){
            System.out.println("x is not equal to 10");
        }
        else{
            System.out.println("x value is not matched");
        }
    }
}
